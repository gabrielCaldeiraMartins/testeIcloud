import Foundation


class Math {
    
    func add(numb1: Int, numb2: Int) -> Int {
        return numb1 + numb2
    }
    
    func sub(numb1: Int, numb2: Int) -> Int {
        return numb1 - numb2
    }
    
    func mul(numb1: Int, numb2: Int) -> Int {
        return numb1 * numb2
    }
    
    func div(numb1: Int, numb2: Int) -> Int {
        return numb1 / numb2
    }
}
